function initI18N(langindex){
	if(langindex == null)
		return;
	var lang = i18n[langindex];
	if(lang != null){
		var elems = document.getElementsByTagName("*");
		for(var i=0; i<elems.length; i++){
			var elem = elems[i];
			var langkey = elem.getAttribute("lang.key");
			var langto = elem.getAttribute("lang.to");
			var langchild = elem.getAttribute("lang.child");
			if(langkey != null && langkey != "" && langchild != ""){
				if(langto == null && langchild == null){
					elem.innerHTML = lang[langkey];
				}
				else if(langchild != null){
					var directArr = langchild.split(";");
					var pairArr = new Array;
					for (var idx = 0; idx < directArr.length; idx++)
					{
						var item = new Object;
						var pos = directArr[idx].indexOf("=");
						item.prop = directArr[idx].substring(0, pos);
						item.value = directArr[idx].substring(pos+1);
						pairArr.push(item);
					}
					var node = elem;
					var failed = false;
					if (node.children.length){
						for (var cnt = 0; cnt < pairArr.length; cnt++)
						{
							for (var j = 0; j < node.children.length; j++)
							{
								if (node.children[j] != null && node.children[j].getAttribute(pairArr[cnt].prop) == pairArr[cnt].value)
								{
									if (cnt == pairArr.length - 1)
									{
										node.children[j].innerHTML = lang[langkey];
									}
									else
									{
										node = node.children[j];
									}
									break;
								}
								else if (j == node.children.length - 1)
								{
									failed = true;
								}
							}
							if (failed)
							{
								break;
							}
						}
					}
					else
					{
						elem.innerHTML = lang[langkey];
					}
				}
				else{
					elem.setAttribute(langto, lang[langkey]);
				}
			}
		}
	}
	window.langindex = langindex;
}

function InitSingleElemLangByID(elementID) {
	var langindex = window.langindex;
	var lang = i18n[langindex];
	if (lang != null) {
		var elem = document.getElementById(elementID);
		if (elem.getAttribute("special.key") == "button") {
			var langkey = elem.getAttribute("lang.key");
			elem.value = lang[langkey];
		} else {
			var langkey = elem.getAttribute("lang.key");
			var langto = elem.getAttribute("lang.to");
			if (langkey != null && langkey != "") {
				if (langto == null) {
					elem.innerHTML = lang[langkey];
				} else {
					elem.setAttribute(langto, lang[langkey]);
				}
			}
		}
	}
}

function selectLang(){
	if(window.langindex == "zh_cn"){
		CommandSupport.sendCommand("i18n", CommandStrings.ChangeLanguage + "[OSD_ENGLISH]");
		initI18N("en_us");
	}
	else{
		CommandSupport.sendCommand("i18n", CommandStrings.ChangeLanguage + "[OSD_SCHINESE]");
		initI18N("zh_cn");
	}
	//EventMgr.fireEvent("i18n", EventObjectSupport.LanguageChanged, window.langindex);
}
